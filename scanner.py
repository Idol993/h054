class PortScanner:
    def __init__(self, rate_limiter: RateLimiter, timeout: int = 2, max_workers: int = 50):
        self.timeout = timeout
        self.max_workers = max_workers
        self.rate_limiter = rate_limiter

    def _scan_port(self, ip: str, port: int) -> Tuple[str, int, bool]:
        self.rate_limiter.wait()
        try:
            sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            sock.settimeout(self.timeout)
            result = sock.connect_ex((ip, port))
            sock.close()
            return (ip, port, result == 0)
        except Exception:
            return (ip, port, False)

    def scan(self, hosts: Set[str], ports: List[int]) -> Dict[str, List[int]]:
        results: Dict[str, List[int]] = {host: [] for host in hosts}
        tasks = [(host, port) for host in hosts for port in ports]
        with ThreadPoolExecutor(max_workers=self.max_workers) as executor:
            futures = {
                executor.submit(self._scan_port, ip, port): (ip, port)
                for ip, port in tasks
            }
            for future in as_completed(futures):
                ip, port, is_open = future.result()
                if is_open:
                    results[ip].append(port)
        for host in results:
            results[host].sort()
        return results
